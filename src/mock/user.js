import Mock from 'mockjs'
import moment from 'moment'


function param2Obj(url) {
  let search = url.split('?')[1]

  if (!search) {
    return {}
  }
  return JSON.parse(
    '{"' +
      decodeURIComponent(search)
        .replace(/"/g, '\\"')
        .replace(/&/g, '","')
        .replace(/=/g, '":"') +
      '"}'
  )
}
